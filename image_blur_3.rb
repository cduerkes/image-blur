class Image
  attr_accessor :array

  def initialize(array)
    self.array = array
  end

  def output_image
    array.each do |row|
      puts row.join
    end
  end

  def blur(dist)
# Blur based on user-specified Manhattan distance. 

    coord = []

    array.each_index do |row|
    subarray = array[row]
      subarray.each_index do |cell|
        coord << [row, cell] if array[row][cell] == 1
      end
    end

    array.each_index do |row|
    subarray = array[row]
      subarray.each_index do |cell|
        coord.each do |y, x|
          array[row][cell] = 1 if (y - row).abs + (x - cell).abs <= dist
        end
      end
    end
  end
end

